export function getDevocionalDoDia() {
  const hoje = new Date();
  const diaDoAno = Math.floor(
    (hoje - new Date(hoje.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24)
  );
  const index = diaDoAno % DEVOCIONAIS.length;
  return DEVOCIONAIS[index];
}
